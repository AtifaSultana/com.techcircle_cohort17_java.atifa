package day03;

public class PrimitiveTypeChar {
	public static void main(String[] args) {
		
		//  dataType variableName = value;
		
		char letter = 'a';
		char letter2 = 's';
		char digit = '4';
		char digit2 = '0';
		
		char digit3 = '1';
		
		System.out.println(letter); // a
		System.out.println(letter2); // s
		System.out.println(digit); // 4
		System.out.println(digit2); // 0
		System.out.println(digit3); // 1
		
		
		
		
		
	}
}

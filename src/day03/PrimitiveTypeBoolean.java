package day03;

public class PrimitiveTypeBoolean {

	public static void main(String[] args) {
		// dataType variableName = value1;
		boolean result = true;
		
		boolean resultForTestCase101 = false;
		
		boolean isSunnyToday = true;
		
		boolean isTimeForBreak = true;
		
		
		System.out.println(result); // true
		System.out.println(resultForTestCase101); // false
		System.out.println(isSunnyToday); // true
		System.out.println(isTimeForBreak); // true
		

	}

}

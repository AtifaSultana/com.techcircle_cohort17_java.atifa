package day14_strings;

public class StringMethod_StartWith_EndWith {

	public static void main(String[] args) {
		
		String str = "US economy";
		
		System.out.println(str.startsWith("U"));
		System.out.println(str.startsWith("US"));
		System.out.println(str.startsWith("USA"));
		
		System.out.println(str.endsWith("my"));
		System.out.println(str.endsWith("my "));
		
		System.out.println(str.startsWith("US economy"));
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}

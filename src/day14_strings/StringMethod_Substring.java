package day14_strings;

public class StringMethod_Substring {

	public static void main(String[] args) {
		
		String str = "television";
		
		String str2 = str.substring(1,5);
		System.out.println(str2);
		
		String str3 = str.substring(4);
		
		System.out.println(str3);
		
		String str4 = "immutable";
		
		System.out.println(str4.substring(2));
		
		
	}

}

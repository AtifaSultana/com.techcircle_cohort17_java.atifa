package day05_operators;

public class DecrementIncrement {

	public static void main(String[] args) {
		
		int x = 10;
		x = x +1;                 // 11
		x++; // x - x + 1         // 12
		++x;                      // 13
		
		System.out.println(x ); // 13
		
		int y = 10;
		y = y -1;                 // 9
		y--;                      // 8
		--y;                      // 7
		
		System.out.println(y); // 7
		
	}

}
